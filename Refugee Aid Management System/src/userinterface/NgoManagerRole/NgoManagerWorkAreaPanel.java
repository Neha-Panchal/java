    public void calculatefood()
    {
       for ( Organization org :this.enterprise.getOrganizationDirectory().getOrganizationList())
       {
           if (org instanceof ResourceCollectorOrganization){
               
               for(WorkAddResourse war: org.getWorkQueue().getAddresourceList())
               {
                   if(war.getSign()==1)
                   this.food+=war.getNumber();
                   else
                       this.food-=war.getNumber();
               }
           }
       }
    }
