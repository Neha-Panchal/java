    public void populatetablereceive()
    {
         DefaultTableModel model = (DefaultTableModel) this.jTable_receive.getModel();
        
        model.setRowCount(0);
        int x=0;
        
                    for (WorkAidRequest war : this.organization.getWorkQueue().getAidrequestList()){
                        if(war.getNgoreciever()==this.userAccount || war.getNgoreciever()==null && war.getRequestsender()!=this.userAccount)
                        {
                         Object[] row = new Object[4]; x++;
                         row[0] = x;
                         row[1] = war.getAmount();
                         row[2] = war;
                         row[3] = war.getRequestsender();
                        
                         model.addRow(row);
                         }
                    }
       
    }
    
