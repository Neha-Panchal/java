    private void SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendActionPerformed
        // TODO add your handling code here:
        
        int sr = jTable1.getSelectedRow();
        if(sr>=0)
        {
            
             WorkRegistration  wr =  (WorkRegistration) jTable1.getValueAt(sr, 1);
             
             if(wr.getUnosender()==null)
             {
                 JOptionPane.showMessageDialog(null,"job Didnt assigned yet"); return;
             }
             if(!wr.getStatus().equals("Manager Received")){ JOptionPane.showMessageDialog(null,"Already Sent"); return;}
             Organization org = null;
             Enterprise epp=null;
             for (Enterprise enterprise : this.network.getEnterpriseDirectory().getEnterpriseList()){
             if (enterprise instanceof EmbassyEnterprise){
                 if(enterprise.getName().equalsIgnoreCase(wr.getCountry()))
                 {
                     epp = enterprise;
                     break;
                 }
                 
               }
              }
             //epp=(Enterprise)this.Combo_enterprise.getSelectedItem();
             if (epp!=null){
             for (Organization organization : epp.getOrganizationDirectory().getOrganizationList()){
             if (organization instanceof ApprovalOrganization){
                org = organization;
                break;
               }
              }
             }else {JOptionPane.showMessageDialog(null,"There is no Embassy"); return;}
             
            if (org!=null){
            org.getWorkQueue().getRegistrationList().add(wr);
            userAccount.getWorkQueue().getRegistrationList().add(wr);
            wr.setStatus("sent to ebmassy");
            
            JOptionPane.showMessageDialog(null,"details sent ");
            }
            else
            {
            JOptionPane.showMessageDialog(null,"There is no ApprovalOrganization");
            } 
            this.populateRequestTable();
        }else
        {
            JOptionPane.showMessageDialog(null,"Please Select a specific detail ");
        }
        
        
        
        
    }//GEN-LAST:event_SendActionPerformed

