    private void Button_analysisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_analysisActionPerformed
        // TODO add your handling code here:
        HashMap<String, Integer> temp = new LinkedHashMap<String, Integer>();
        HashMap<String, Integer> a2 = new LinkedHashMap<String, Integer>();
        HashMap<String, Integer> per = new LinkedHashMap<String, Integer>();
        for (WorkRegistration register : this.organization.getWorkQueue().getRegistrationList())
        {
            int i =1;
            if(temp.containsKey(register.getCountry()))
            {
                i=temp.get(register.getCountry()); i++;
            }
            temp.put(register.getCountry(), i);
        }
        
        for (WorkRegistration register : this.organization.getWorkQueue().getRegistrationList())
        {
            int i =0;
            if(a2.containsKey(register.getCountry()))
            {
                if(register.getStatus()!=null)
                {
                    if(register.getStatus().equals("approved"))
                    {
                    i=a2.get(register.getCountry()); 
                    i++;}
                }
            }
            else if(register.getStatus()!=null)
                {
                    if(register.getStatus().equals("approved"))
                    {
                    a2.put(register.getCountry(), 1);
                    }
                }
            
        }
        for (Map.Entry<String, Integer> entrySet : a2.entrySet())
        {
            int i=temp.get(entrySet.getKey());
            if(i==0)
            per.put(entrySet.getKey(),0 );
            else
            {
                int x=entrySet.getValue()*100/i;
      
                 per.put(entrySet.getKey(), x );
            }
               
        }
        
        Charts build = new Charts("Analysis", "Number of Applications To Countries", temp);
        build.pack();
        build.setVisible(true);
        Charts build1 = new Charts("Analysis", "Approved Percentage From Countries", per);
        build1.pack();
        build1.setVisible(true);
        
    }//GEN-LAST:event_Button_analysisActionPerformed


